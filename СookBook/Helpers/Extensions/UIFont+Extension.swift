import UIKit

// MARK: - extension UIFont
extension UIFont {
    // Regular
    static func poppinsRegular10() -> UIFont?{
        return UIFont(name: "Poppins-Regular", size: 10)
    }

    static func poppinsRegular12() -> UIFont?{
        return UIFont(name: "Poppins-Regular", size: 12)
    }
    
    static func poppinsRegular14() -> UIFont?{
        return UIFont(name: "Poppins-Regular", size: 14)
    }
    
    static func poppinsRegular16() -> UIFont?{
        return UIFont(name: "Poppins-Regular", size: 16)
    }
    
    // Bold
    static func poppinsBold12() -> UIFont?{
        return UIFont(name: "Poppins-SemiBold", size: 12)
    }
    
    static func poppinsBold14() -> UIFont?{
        return UIFont(name: "Poppins-SemiBold", size: 14)
    }
    
    static func poppinsBold16() -> UIFont?{
        return UIFont(name: "Poppins-SemiBold", size: 16)
    }
    
    static func poppinsBold20() -> UIFont?{
        return UIFont(name: "Poppins-SemiBold", size: 20)
    }
    
    static func poppinsBold24() -> UIFont?{
        return UIFont(name: "Poppins-SemiBold", size: 24)
    }
    
    static func poppinsBold56() -> UIFont?{
        return UIFont(name: "Poppins-SemiBold", size: 56)
    }
}
